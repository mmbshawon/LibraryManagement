						<h4>Sharing</h4>
						<p>How can I make it easier for others to re-use the materials that I produce?<br><br>
						One relatively simple way to make it easier for others to re-use tools, data, or other content that you produce is to add a Creative Commons license. For example ‘By-Attribution, Non-Commercial’ is a common Creative Commons license – when you mark your file, image, or information with this, it means that anyone can use your information in any way they like, so long as they attribute it to you and don’t use it for commercial purposes. There are other types of Creative Commons licenses, also allowing commerical use, and licesnse which do not require re-user to attribute the creator. Creative Commons licenses are often used for materials released online, but you can also include these in printed materials if your publisher does not own the rights. For additional information about Creative Commons license options, visit their website or watch the short video below:<br><br>
						To license something with a Creative Commons license, you don't need to file any paperwork - just publish (in print or on the web) your materials along with a notification that you are using a particular license.<br><br>
						<b>IMPORTANT NOTE:</b> Creative Commons licenses are 'irrevocable' so do not add a Creative Commons license unless you are sure that:<br><br>
						you have the right to publish this information<br><br>
						you will not want to revoke it later on for any reason.</p>
