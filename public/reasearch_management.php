						<a href="#">Organising E-mail</a></p>
						<h4>Naming and Organising files</h4>
						<p>Choosing a logical and consistent way to name and organise your files allows you and others to easily locate and use them. Ideally, the best time to think how to name and structure the documents and directories you create is at the start of a project.<br><br>
						Agreeing on a naming convention will help to provide consistency, which will make it easier to find and correctly identify your files, prevent version control problems when working on files collaboratively. Organising your files carefully will save you time and frustration by helping you and your colleagues find what you need when you need it.</p>
