						<h4>First steps</h4>
						<p>There are many decisions to make about managing your data before you even start creating/collecting it. These include formal or semi-formal data planning (many funders now require data management plans to be submitted with grant applications), choosing hardware and software, and addressing issues related to intellectual property rights and ethics. Decisions made at the beginning (or as early as possible) will affect how you can access, use, or preserve your data in the future. Your best starting point is to create a data management plan - whether your funder requires you to prepare one or not. A proper data management could be a useful resource to you as a guidance map to all your research datasets. You might also wish to keep your data management plan up to date in case your funder has any questions about your data sharing activities.<br><br>
						Additionally, creating a data management plan will also help you to highlight any regulatory issues that you might need to consider before even starting to collect your data. For example, if you intend to conduct interviews, have you asked your interviewees for a permission to record and re-use collected information?  A proper data management plan created at the beginning of the project, can save you a lot of time during data collection, and also when consolidating your data towards the end of the project.<br><br>
