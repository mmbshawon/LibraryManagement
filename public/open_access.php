										<h4>Who is affected</h4>
										<p>Every Cambridge author who is submitting their work in the next REF, and only for articles or conference proceedings (with an ISSN).</p>
										<h4>Which manuscripts to upload</h4>
										<p>Upload articles or conference proceedings (with an ISSN). Monographs, books and other book chapters do not need to be uploaded.</p>
										<h4>Who can upload manuscripts</h4>
										<p>Manuscripts can be uploaded by an author, or an administrator or librarian on their behalf. You will need a Raven account (CRSid) to upload a manuscript.<br><br>
										Each co-author should upload their accepted manuscripts. We will detect duplicates after papers have been uploaded.</p>
										<h4>Which version to upload</h4>
										<p>You should upload your Author’s Accepted Manuscript. This is your version of the manuscript after peer review and including any final changes, but before publisher typesetting or copy-editing by the publisher.<br><br>
										Please do not send final versions, or typeset proofs.<br><br>
										You should upload the manuscript before you sign the publisher’s Copyright Transfer Agreement. Find out more about your copyright options.</p>
										<h4>When you need to upload</h4>
										<p>As soon as your peer-reviewed manuscript has been accepted, upload it. HEFCE policy requires manuscripts to be uploaded no later than three months after acceptance.<br><br>
										Ideally you should upload the manuscript, or contact us, before signing a Copyright Transfer Agreement or choosing an Open Access option on the journal website.</p>
										<h4>Your copyright options</h4>
										<p>Your rights will vary depending on your publisher. Many Copyright Transfer Agreements already allow you to comply with HEFCE and some other funders’ Open Access policies.<br><br>
										Publishers may choose to licence back some rights in your work e.g. for use in teaching or to promote your research. You may prefer to retain your rights, rather than give them to the publisher and try to get them back later.<br><br>
										You have two copyright options;<br>
										<b>1. Sign a Copyright Transfer Agreement</b><br><br>
										A Copyright Transfer Agreement (CTA) may give your publisher copyright and might affect how and when you can make your manuscript Open Access.<br>
										We’ll check your publisher’s model for licensing and Open Access when you upload your manuscript.<br><br>
										<b>2. Use a Scholar’s Addendum</b><br>
										The University of Cambridge Scholars' Addendum enables you to change the traditional agreement you have with your publisher. You still transfer copyright, but include a proviso covering digital usage and access.<br>
										How to use the Addendum:<br>
										Download the Cambridge Scholars' Addendum.<br>
										Print the Addendum and add the manuscript and journal titles, corresponding author and publisher names.<br>
										Date and sign the agreement.<br>
										Scan and email the agreement to the publisher. Ask them to sign and append it to their Copyright Transfer Agreement.<br>
										If you need advice and guidance on how to approach your publisher and use the Addendum with your existing agreement, please contact us.<br>
										There are alternative Addendums available here .<br>
										I’ve already signed an agreement<br><br>
										If you can, it’s better to contact us before you sign an agreement. If you’ve already signed we can check whether the agreement allows you to comply with HEFCE and your other funders’ Open Access policies.<br>
										If it doesn’t comply, we can liaise with your publisher and help you negotiate amended rights. If you need advice and guidance please contact us.</p>
										<h4>Licences</h4>
										<p>Licences are used to tell the reader what rights they are being given in relation to the paper/article when they access it. Different publishers offer a range of licensing agreements. Licences affect how your work can be copied, distributed, shared and how others can use it, commercially and non-commercially.<br>
										Some funders (e.g. RCUK, Wellcome Trust) specify what licence Open Access content should be published under. If this applies to your article, we'll check whether your journal offers the right licence and advise you if you need to take any further action.</p>
