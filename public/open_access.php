		                        		<h4>Why Open Access?</h4>
		                        		<p>The move towards Open Access is motivated by two things; increasing access to published research and the new policies established by HEFCE and other funders, including RCUK and The Wellcome Trust.<br>
		                        		This service is designed to help you comply with your funders' Open Access policies and ensure your work is eligible for the REF.</p>
		                        		<h4>Benefits of Open Access</h4>
		                        		<p>Open Access removes restrictions to view and read a work and allows reuse, redistribution and modification of the work with attribution.<br>
		                        		For the author and researcher, Open Access enables them to raise the visibility and profile of their research and to increase its impact.<br>
		                        		It also increases access to research, widening the spread of knowledge and ideas.</p>
		                        		<h4>Open Access publishing</h4>
		                        		<p>There are two main models for Open Access; self-archiving versus pay to publish.<br><br>
		                        		<b>1. Self-archiving (Green Open Access)</b><br><br>
		                        		The deposit of peer-reviewed manuscripts in an Open Access repository. Publishers often stipulate a fixed period of time before the article can be made freely available (an embargo).<br><br>
		                        		What does self-archiving mean for you?<br><br>
		                        		*** An increase in the availability and visibility of your work<br>
		                        		*** It removes the need to pay Open Access charges<br><br>
		                        		The University prefers the self-archiving model. Find out more about uploading your manuscript.<br><br>
		                        		<b>Embargo</b><br><br>
		                        		The embargo is a fixed period of time a publisher might establish to delay the Open Access publication of a manuscript. These embargo periods may differ in length from 6 - 36 months and vary in subject fields. This will affect when your manuscript will be able to be viewed and accessed. We can help you find out how a publisher’s embargo might affect you.<br><br>
		                        		<b>2. Pay to publish (Gold Open Access)</b><br><br>
		                        		Free access to the final published versions of articles, immediately on publication. The publisher requires an Article Processing Charge which includes Open Access charges.<br><br>
		                        		What does this mean for you?<br><br>
		                        		*** Provides immediate access to journals as soon as they are published<br>
		                        		*** Meets the requirement of certain funders.<br><br>
		                        		The University and funders may help in the payment of this. Find out more</p>
