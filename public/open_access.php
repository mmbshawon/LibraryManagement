										<h4>When do I need to do something?</h4>
										<p>As soon as your peer-reviewed manuscript has been accepted, upload it. HEFCE policy requires manuscripts to be submitted within 3 months of the acceptance date.<br><br>
										This policy comes into force on 1 April 2016, yet we want to ensure this shift is managed sooner rather than later, so that no research is omitted.<br><br>
										Uploading at acceptance lets us check your options and advise you how to fill in copyright or Open Access forms to comply with other funders’ requirements, and – in eligible cases – to commit funds to cover Open Access fees.</p>
										<h4>Research Excellence Framework (REF)</h4>
										<p>By uploading your peer-reviewed manuscript as soon as it is accepted by a publisher, we’ll make sure you comply with HEFCE’s Open Access policy for the next REF and deposit it in the institutional repository.<br><br>
										Publisher embargoes are respected (the period when you can only access an article with a paid access). Manuscripts must be free to read and download after the following periods:<br><br>
										12 months for REF Main Panel A and REF Main Panel B<br>
										24 months for REF Main Panel C and REF Main Panel D7</p>
										<h4>How does this apply to other funders?</h4>
