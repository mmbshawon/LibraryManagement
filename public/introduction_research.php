				<h4>Evaluating resources</h4>
                <p>When using a book, article, report, or Web site for your research, it is important to gauge how reliable the source is.<br><br>
				<b>Initial appraisal</b><br><br>
				Author or creator: What are the author's credentials (educational background, past writing, experience) in this area? Have you seen the author's name cited in other sources or bibliographies? Respected authors are cited frequently by other scholars. For this reason, always note names that appear in many different sources.<br><br>
				Year of publication: Is the source current or out of date for your topic? Topic areas of continuing and rapid development, such as the sciences, demand more current information. Topics in the humanities often require material that was written many years ago.<br><br>
				Edition: Is this a first edition? Later editions indicate a source has been revised and updated. Multiple printings or editions may indicate that the work has become a standard source in the area and is reliable.<br><br>
				Publisher: Is it a university press or a large reputable publisher?</p>
				<p><b>Content analysis</b><br><br>
				<b>Intentions:</b> Read the preface (book) or abstract (article) to determine the author's intentions. Scan the table of contents and the index to get a broad overview of the material covered. Note whether bibliographies are included.<br><br>
				<b>Intended audience:</b> What type of audience is the author addressing? Is this source too elementary, too technical, too advanced, or just right for your needs?<br><br>
				<b>Objective reasoning:</b><br>
				Is the information fact, opinion, or propaganda? It is not always easy to separate fact from opinion.<br>
				Does the information appear to be valid and well-researched, or is it questionable and unsupported by evidence?<br>
				Is the author's point of view objective and impartial? Is the language free of emotion-rousing words or bias?<br><br>
				<b>Coverage:</b><br>
				Does the work update other sources, substantiate other materials you have read, or add new information? You should explore enough sources to obtain a variety of viewpoints.<br>
				Is the material primary or secondary in nature? Primary sources are the raw material of the research process; secondary sources are based on primary sources.<br>
				Writing style: Is the publication organized logically?<br>
				Are the main points clearly presented? Do you find the text easy to read? Is the author repetitive?<br><br>
				<b>Evaluative reviews (books):</b><br><br>
				Locate critical reviews of books in a reviewing source, such as Book Review Digest. Is the book considered a valuable contribution to the field? Does the reviewer mention other books that might be better? If so, locate these sources.<br>
				Do the various reviewers agree on the value or attributes of the book, or has it aroused controversy among the critics?</p>
				<h4>Evaluating Web resources</h4>
				<p><b>Purpose</b><br>
				Occasionally, Web sites pretending to be objective have a hidden agenda and may be trying to persuade, promote, or sell something.<br>
				What is the purpose or motive for the site? (e.g., educational, commercial, entertainment, promotional)<br>
				Is the site trying to sell you something?<br>
				How easy is it to differentiate advertisement from content?<br>
				Based on your knowledge, is the information factual, opinion, propaganda, et cetera?<br>
				Who is the intended audience, and how is this reflected in the organization and presentation of the site?<br><br>
				<b>Authority</b><br>
				Is the author identifiable? Look for links that say "Who We Are," "About This Site" or something similar.<br>
				Is there contact information for the author? (e.g., e-mail address, mailing address, phone number)<br>
				What is the author's background? (e.g., experience, credentials, occupation, whether he or she has written other publications on the topic)<br>
				Does the author cite his or her sources?<br>
				Is this site linked to often by other sites?<br>
				Do links on this site lead to other reputable sites?<br>
				Are there spelling errors or incorrect use of grammar?<br>
				What domain does the site belong to? (e.g., .edu, .gov, .com, .net, .org)?<br><br>
				<b>Reliability</b><br>
				The dependability of a Web site is important if it is going to be cited as a source in other works or recommended for use by others.<br>
				Do most of the links on the page work?<br>
				From your evaluation of currency and authority, do you think the site will be there next time you visit it?<br><br>
				<b>Currency</b><br>
				When was site last updated or revised? If you cannot find a date on the page, type javascript:alert(document.lastModified) in the address bar and hit Enter. A pop-up window will display the date and time when the page was last updated.<br>
				How often is the site updated?<br>
				Do the links on the site work?<br><br>
				<b>Coverage</b><br>
				What information is included or omitted?<br>
				Is the page completed or under construction?<br>
				See also: University of California Berkeley's guide.</p>
				<h4>Distinguishing scholarly from non-scholarly periodicals (articles and papers):</h4>
                <p>Journals and magazines are important sources for up-to-date information in all disciplines. In this guide we have divided periodical literature into four categories:<br>
				Scholarly<br>
				Substantive news or general interest<br>
				Popular<br>
				Sensational<br>
				Scholarly</p>
				<p><b>Scholarly</b><br><br>
				journals generally have a sober, serious look. They often contain many graphs and charts but few glossy pages or exciting pictures.<br>
				Scholarly journals always cite their sources in the form of footnotes or bibliographies.Articles are written by a scholar or someone who has done research in the field.<br>
				The language of scholarly journals is that of the discipline covered. It assumes some scholarly background on the part of the reader.<br>
				The main purpose of a scholarly journal is to report on original research or experimentation to make the information available to the rest of the scholarly world.<br>
				Examples of scholarly journals: American Economic Review, Archives of Sexual Behavior, JAMA: The Journal of the American Medical Association, Modern Fiction Studies</p>
				<p><b>Substantive news or general interest</b><br><br>
				These periodicals may be quite attractive in appearance. Some are in newspaper format. Articles are often heavily illustrated and generally contain photographs.<br>
				News and general interest periodicals sometimes cite sources, a scholar, or a freelance writer.<br>The language of these publications is geared to any educated audience. There is no special training assumed, only interest and a certain level of intelligence.<br>
				They are generally published by commercial enterprises or individuals, although some come from professional organizations.<br>
				The main purpose of periodicals in this category is to provide general information to a broad audience of concerned citizens.<br>
				Examples of substantive news or general-interest periodicals: The Economist, National Geographic, The New York Times, Scientific American</p>
				<p><b>Popular</b><br><br>
				Popular periodicals come in many formats, although they are often somewhat slick and attractive in appearance and have many graphics.<br>
				These publications rarely, if ever, cite sources. Information published in such journals is often second- or third-hand, and the original source is sometimes obscured.<br>
				Articles are usually very short, written in simple language, and designed to meet a minimal education level. There is generally little depth to the content of these articles.<br>
				Articles are written by staff members or freelance writers.<br>
				The main purpose of popular periodicals is to entertain the reader, sell products (their own or their advertisers'), and/or promote a viewpoint.<br>
				Examples of popular periodicals: Ebony, Parents, People, Reader's Digest, Sports Illustrated, Time, Vogue</p>
				<p><b>Sensational</b><br><br>
				Sensational periodicals come in a variety of styles but often use a newspaper format.<br>
				The language is elementary and occasionally inflammatory or sensational. They assume a certain gullibility in their audience.<br>
				The main purpose of sensational magazines seems to be to arouse curiosity and cater to popular superstitions. They often do so with flashy headlines designed to astonish (e.g., "Half-man Half-woman Makes Self Pregnant").<br>
				Examples of sensational periodicals: The Globe, The National Enquirer, The Star, Weekly World News</p>
