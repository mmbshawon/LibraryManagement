				<h4>Choosing and developing a research topic</h4>
				<div class="well">
					<h5>Suggestions for finding a topic</h5>
					<ul>
						<li>Discuss your ideas with your course instructor.</li>
						<li>Discuss your ideas with a reference librarian.</li>
						<li>Look over the index and the article titles in a specialized encyclopedia that covers a relevant subject area or discipline.</li>
					</ul>
