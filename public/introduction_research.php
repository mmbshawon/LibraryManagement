				<p><b>Identifying a topic</b><br><br>
				State your topic idea as a question. For example, if you are interested in finding out about Title IX (Title Nine) and women athletes in college athletic programs, you might pose the question, "How did Title IX impact women athletes in college athletic programs?"<br><br>
				Identify the main concepts or keywords in your question. These are potential search terms. In this case they are "title ix," "women," "athletes," and "college athletic programs".</p>
				<p><b>Testing the topic</b><br><br>
				Before you commit to a specific topic for your research, do a scan to make sure that your topic isn't completely covered in another paper; at the same time ensure that there is enough information available to complete the project. This can be particularly important if you are planning on using data in your research. If in doubt, ask your professor.<br><br>
				If you are finding too much information and too many sources, narrow your topic. For example: "women and athletes and college and athletics".<br><br>
				Finding too little information may indicate that you need to broaden your topic by using a more general term or terms in your search.</p>
				<p><b>Finding background information</b><br><br>
				Once you have identified the main topic and keywords for your research, find one or more sources of background information to read. These sources will help you understand the broader context of your research and tell you in general terms what is known about your topic. They will give you an idea of how much and what kind of information is available on a given topic.<br><br>
				Encyclopedias and dictionaries: You can find subject-specific encyclopedias and dictionaries by using the Library Catalog or by asking a reference librarian. You may wish to also consult Reference Universe Online, Wikipedia, or our list of Dictionaries and Encyclopedias.<br><br>
				Exploit bibliographies: Often there are scholarly articles that give an overview of research in specific fields (a review of the literature). The sources cited in the bibliography are good starting points for further research.<br><br>
				Look up these sources in the Library Catalog. Check the subject headings listed in the subject field of the online record for these books and journals. Then do subject searches using those subject headings to locate additional titles.</p>
