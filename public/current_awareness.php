                <h3 class="text-center">Current Awareness</h3>
                <p>It is vital for researchers and academics to keep up-to-date with the most recently published information and developments. A selection of current-awareness services is outlined below. Need more help? Ask A Librarian.</p>
				<h4>New Books at Cornell</h4>
				<p>New Books at Cornell allows you to browse new library books by month, individual library, language, and subject classification. You can also create a customized RSS feed to get monthly updates.</p>
				<h4>Table of Contents (TOC) alerts</h4>
				<p>Table of Contents alerts allow you to be automatically notified when the new issue of a journal is published.<br><br>
				<b>JournalTOCs</b><br><br>
				The JournalTOCs Tables of Contents service allows you to keep up-to-date with newly published scholarly material by enabling you to browse, view, save, and search across thousands of journal tables of contents from hundreds of publishers. Free registration allow you to create a customized list of your most important and favorite journals, and includes export options such as email alerts, RSS feeds, formats for bibliographic managers, and customizable API for web pages. JournalTOCS will be replacing ticTOCs and existing ticTOCs users must re-register with the JournalTOCs service. Note: there is a limit of 30 journal titles that can be followed.<br><br>
				<b>E-mail (or RSS) alerts from databases subscribed to by the Cornell University Library</b><br><br>
				Examples of vendor databases that include journals from multiple publishers and provide TOC e-mail alerts include:<br><br>
				### EBSCO databases - click on "Publications" link from the toolbar. Browse for journal title and select. Click ""Alert/Save/Share" link on publications page.<br><br>
				### ProQuest databases - click "Publications" tab. Browse for journal title and select. Click "set up alert" or "create RSS feed" from publication page.<br><br>
				### ISI's Web of Science - conduct a "publication name" search. Click on "Search History". Click "Save History/Create Alert" button and choose frequency of alert (options are weekly or monthly).<br><br>
				### Publisher-provided TOC alerts - Another option is to sign up for TOC alerts sent directly from publishers of journals or article databases to which Cornell University subscribes. This usually requires creating an individual profile at a publisher's Web site.</p>
				<h4>Blogs</h4>
				<p>Blogs began as online personal journals, but they have emerged as broad Web tools with many applications -- and they're some of the most effective current awareness tools, because they allow for direct participation and feedback and are often updated frequently. In conjunction with RSS feeds, most blogs offer a subscription feature that allows for content to be sent directly to subscribers.<br><br>
				<b>Cornell Library staff maintain several blogs, including:</b><br><br>
				Shelf Life: the Cornell Chronicle's blog on all things Library<br>
				Problem Solved: the blog for the Engineering, Math, and Physical Sciences (EMPS) Libraries<br>
				@ Olin & Uris Libraries: what's up on the south side of the Arts Quad<br>
				History @ CUL: news of interest to historians from the Cornell Library<br>
				EnglishLit @ CUL: a current awareness blog for Cornellians engaged in the study of English-language literature and associated subjects<br>
				Government Studies @ the Library: for the students and faculty of the Cornell Department of Government</p>
				<h4>RSS Feeds</h4>
				<p>RSS (some people say it stands for Rich Site Summary, some people say it stands for Really Simple Syndication) is a format for publishing web content. It's used to "push" timely information and updates to people who subscribe to RSS "feeds". What's so cool about it is you can collect a bunch of feeds in one place (your reader or news aggregator), log in whenever you like, and see what's new. You don't have to visit all the web sites one at a time, and you don't have to waste your time with old news.<br><br>
				See the library's guide to Keeping Current with News and Research for a more detailed explanation of RSS.</p>
				<h4>Facebook & Twitter</h4>
				<p>Social networking tools, such as Facebook and Twitter, help you keep up with the day-to-day operations of the Library. Facebook is the world's most popular social networking tool, and Twitter is a microblogging platform that uses short, mobile-accessible updates.<br><br>
				<b>Some of the Library's Facebook pages include:</b><br><br>
				Cornell University Library<br>
				The Division of Rare and Manuscript Collections<br>
				The Human Sexuality Collection<br>
				The Hip Hop Collection<br>
				Cornell Hotel School Library<br><br>
				<b>Examples of Cornell Library Twitter accounts include:</b><br><br>
				Cornell University Library news<br>
				Olin Library<br>
				Nestlé Library (Hotel School)<br>
				Management Library (Johnson School)<br><br>
				<b>Social Bookmarking (and beyond)</b><br><br>
				"Have you read this new article?" Word of mouth between colleagues is an excellent way to keep current. Several services attempt to replicate this experience online.<br>
				Social Bookmarking sites allow you to save references and bookmarks to an online account, which you may choose to share with others. You may also browse by subject or tags, and some services allow you to upload files. The following services, in particular, are intended for academic use:<br><br>
				CiteULike<br>
				Bibsonomy<br><br>
				Some citation management programs allow you to share reference lists:<br><br>
				Zotero - share references with your Zotero contacts
				Mendeley - share references and files with small group of contacts, and view aggregate statistics in your field</p>
				<h4>Citation Alerts</h4>
				<p>citation alert notifies you when new publications cite a particular work. ISI’s Web of Knowledge offers this service if users create personal profiles.<br><br>
				<b>Other Alerts</b><br><br>
				<b>Subject alerts</b><br>
				Subject alerts allow you to be notified when articles are published that match your subject criteria. For example, after conducting a search in a ProQuest databasefor the terms “wind power” and “local government”, you could set up an alert to be notified of any new articles that get added to the database with those keyword terms. This service is also available in databases produced by ISI’s Web of Knowledge, EBSCOhost, CSA Illumina, Elsevier (ScienceDirect), Google (Google Scholar) and more. For more details, see the Cornell University Library guide for Creating Database Search Alerts.</p>
				<h4>Web page alerts</h4>
				<p>Stay up-to-date with new online content in your subject area. There are a number of services that provide alerting services for new publications on the Web, including:<br><br>
				Google Alerts tracking service for search-engine results that watches for online new content by monitoring Web pages indexed by Google and e-mails users when it locates new items<br>
				The Scout Report - weekly reports offering a selection of new and newly discovered Web resources of interest to researchers and educators<br>
				Conferences - Locating papers delivered at conferences can sometimes be difficult, but they are often the only record of vital new research results. Some conference listings include:<br><br>
				llconferences.com<br>
				Conference Alerts</p>
